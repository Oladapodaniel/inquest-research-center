<script setup>
import { onMounted, ref } from 'vue';
import Dropdown from 'v-dropdown'
// Initialization for ES Users
import {
  Collapse,
  initTE,
  Ripple
} from "tw-elements";


defineProps({
  msg: String,
})

const dropdowntheme = ref(null)

const setDarkTheme = () => {
  document.documentElement.classList.add("dark");
  localStorage.theme = "dark";
};

const setLightTheme = () => {
  document.documentElement.classList.remove("dark");
  localStorage.theme = "light";
};

const onThemeSwitcherItemClick = (event) => {
  if (event === "system") {
    localStorage.removeItem("theme");
    setSystemTheme();
  } else if (event === "dark") {
    setDarkTheme();
  } else {
    setLightTheme();
  }
};

const themeSwitcherItems = document.querySelectorAll("#theme-switcher");
themeSwitcherItems.forEach((item) => {
  item.addEventListener("click", onThemeSwitcherItemClick);
});

const themeToggler = (item) => {
  onThemeSwitcherItemClick(item)
  dropdowntheme.value.close();
}

onMounted(() => {
  initTE({ Collapse, Ripple });
})
</script>

